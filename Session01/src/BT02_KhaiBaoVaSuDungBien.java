public class BT02_KhaiBaoVaSuDungBien {
    public static void main(String[] args) {
        /*
        Khai báo các biến sau và hiển thị giá trị của chúng ra màn hình:

        Biến i kiểu số nguyên, có giá trị là 10

        Biến f kiểu số thực, có giá trị là 20.5

        Biến d kiểu số thực lớn, có giá trị là 20.5

        Biến b kiểu logic, có giá trị là true

        Biến c kiểu ký tự, có giá trị là 'a'

        Biến s kiểu chuỗi, có giá trị là "Hà Nội".
        */
        int i=10;
        float f=20.5F;
        double d=20.5F;
        boolean b=true;
        char c='a';
        String s="Hà Nội";
        System.out.println("Biến i kiểu số nguyên, có giá trị là "+i);
        System.out.println("Biến f kiểu số thực, có giá trị là "+f);
        System.out.println("Biến d kiểu số thực lớn, có giá trị là "+d);
        System.out.println("Biến b kiểu logic, có giá trị là "+b);
        System.out.println("Biến c kiểu ký tự, có giá trị là "+c);
        System.out.println("Biến s kiểu chuỗi, có giá trị là "+s);

    }
}
